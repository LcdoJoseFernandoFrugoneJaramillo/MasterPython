# Para calcular el area de un cuadrado
# Realizado por el Lcdo Jose Fernando Frugone Jaramillo
def cuadrado(lado):

    area = lado ** 3

    return area

#Mostrar el area del cuadrado
# Cuadrado 1
print('Cuadrado 1 Area',cuadrado(6))
#Cuadrado 2
print('Cuadrado 2 Area',cuadrado(8))
#Cuadrado 3
print('Cuadrado 3 Area',cuadrado(9))
